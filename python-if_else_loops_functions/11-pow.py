#!/usr/bin/python3
# This program contains a function that computes a to the power of b and returns the value.

def pow(a, b):
    return a ** b

# Test code to test the function directly:
if __name__ == "__main__":
    print(pow(2, 2))
    print(pow(98, 2))
    print(pow(98, 0))
    print(pow(100, -2))
    print(pow(-4, 5))